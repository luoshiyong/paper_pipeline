class Block(nn.Module):
    def __init__(self, hidden_size,mlp_dim,num_heads,dropout_rate=0.2,attention_dropout_rate=0.2,attention_type='atten',vis=False):
        super(Block, self).__init__()
        self.hidden_size = hidden_size
        self.attention_norm = LayerNorm(hidden_size, eps=1e-6)
        self.ffn_norm = LayerNorm(hidden_size, eps=1e-6)
        self.ffn = Mlp(hidden_size,mlp_dim,dropout_rate)
        if attention_type=='atten':
            self.attn = Attention(hidden_size,num_heads,attention_dropout_rate,vis)
        elif attention_type=="axis_atten":
            self.attn = AxialBlock(inplanes=512, planes=512,groups=8,base_width=64,dilation=1,kernel_size=14)
        else:
            self.attn = Attention(hidden_size, num_heads, attention_dropout_rate, vis)

    def forward(self, x):
        h = x
        x = self.attention_norm(x)
        x, weights = self.attn(x)
        x = x + h

        h = x
        x = self.ffn_norm(x)
        x = self.ffn(x)
        x = x + h
        return x, weights
class Encoder(nn.Module):
    def __init__(self, mlp_dim,num_heads,hidden_size=256,num_layers=12, vis=False):
        super(Encoder, self).__init__()
        self.vis = vis
        self.layer = nn.ModuleList()
        self.encoder_norm = LayerNorm(hidden_size, eps=1e-6)
        for _ in range(num_layers):
            layer = Block(hidden_size,mlp_dim,num_heads,vis)
            self.layer.append(copy.deepcopy(layer))

    def forward(self, hidden_states):
        attn_weights = []
        for layer_block in self.layer:
            hidden_states, weights = layer_block(hidden_states)
            if self.vis:
                attn_weights.append(weights)
        encoded = self.encoder_norm(hidden_states)
        return encoded, attn_weights
